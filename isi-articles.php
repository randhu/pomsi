<h3>NL19-7007134, Jozef of the Eijerkamp family</h3>

<p>This excellent racing bird was 1st National ace pigeon one day long distance WHZB/TBOTB 2000, 1st National ace pigeon middle distance Allerbeste/NPOrgaan and 1st National ace pigeon one day long distance PIPA. The sire (Solange Jr.) of Jozef is a super class breeder for the Eijerkamp family, since Solange Jr. is the sire of several other successful racing birds in Brummen, besides Jozef. Solange Jr. was bred from NL10-1682373/Tyson x NL07-1817923/Olympic Solange. And that same pair also bred Son Solange of the Derksen-vd Keuken combination, which has bred many successful pigeons for this combination.<br />
The Eijerkamp family also won a title of 15th National ace pigeon Allround with Annemie. This pigeon stems from Catherine, a daughter of NL11-1741053/Ferrari (zoon Olympic Solange) x NL10-1682216/Olivia. Annemie won for instance a 6th NPO Chateaudun (4,058 p. and a 10th NPO Chateauroux (3,794 p.). Aberdeen was 21st National ace pigeon Allround, and Aberdeen was bred from Panamera, which is a son of NL13-1239600/Topper Harley x NL15-1706407/Princess. Aberdeen won a 4th National S3 Fay aux Loges (14,036 p.). It was in part with the help of these and other Verkerk pigeons that they became 1st and 2nd best fancier WHZB/TBOTB 2020.</p>

