            <div class="clearfix text-formatted field field--name-field-text field--type-text-long field--label-hidden field__item"><h3>The Far East</h3>

<p>Fanciers from the other side of the globe, mainly in China and Taiwan, have been winning races and ace pigeon titles in 2020 with descendants of the pigeon family of Gerard and Bas Verkerk as well. The family of Olympic Solange appears a lot in these pedigrees. Besides, the nest sister of the sire of New Olympic Solange is the dam of a first prize winner in Xian. And a top breeder from NL14-1059447/Rico x NL15-1706407/Princess has bred a champion as well. Breeding pair NL09-1186402/Goofy x NL08-1537203/Cameron Diaz has a solid reputation in China as well, given the achievements of their descendants.</p>

<p>We could say that their key to success was not a specific approach, their lofts or the location of their loft but rather the enormous breeding potential of the Verkerk pigeon. No other pigeon family has such an impressive track record.</p>

